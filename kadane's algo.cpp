class Solution {
  public:
    // Function to find the sum of contiguous subarray with maximum sum.
    int maxSubarraySum(vector<int> &arr) {
        int currsum=0 , maxsum=INT_MIN;
        int n =arr.size();
        for(int i =0;i<n;i++){
            currsum+=arr[i];
            maxsum=max(currsum,maxsum);
            if(currsum<=0)
            currsum=0;
        }
        return maxsum;
    }
};

